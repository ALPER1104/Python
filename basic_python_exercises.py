# Kullanıcıdan alınan ismin, şehrin ve mesleğin karakter sayısının ekrana yazdıran bir program yazınız.

ad = input("Lütfen isminizi giriniz: ")
sehir = input("Lütfen yaşadığınız şehrin adını giriniz:")
